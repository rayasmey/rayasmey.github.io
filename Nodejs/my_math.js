const add = function (x, j) {
    return x + j;
}
function subtract(x, y) {
    return x - y;
}
function multiply(x, y) {
    return x * y;
}
function divide(x, y) {
    return x / y;
}
const pi = 3.14;

module.exports = {
    add: add,
    subtract: subtract,
    multiply: multiply,
    divide: divide,
    pi: pi
};